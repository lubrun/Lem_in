#include "libft.h"

void	ft_striter(char *str, void (*f)(char *))
{
	int		n;

	if (str == NULL || f == NULL)
		return ;
	n = 0;
	while (str[n] != '\0')
	{
		(*f)(&str[n]);
		n++;
	}
}
