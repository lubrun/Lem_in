#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s)
	{
		if ((int)*s == c)
			return ((char *)s);
		s++;
	}
	if ((int)*s == c)
		return ((char *)s);
	return (NULL);
}
