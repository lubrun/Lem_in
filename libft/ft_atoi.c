int		ft_atoi(char *str)
{
	int		x;
	int		res;
	int		pos;

	x = 0;
	res = 0;
	pos = 1;
	while (str[x] == '\t' || str[x] == '\v' || str[x] == '\n'
			|| str[x] == '\r' || str[x] == '\f' || str[x] == ' ')
		x++;
	if (str[x] == '+' && str[x + 1] != '-')
		x++;
	if (str[x] == '-')
	{
		pos = -pos;
		x++;
	}
	while (str[x] >= '0' && str[x] <= '9')
	{
		res = (res * 10) + (str[x] - '0');
		x++;
	}
	return (res * pos);
}
